    public void connectAndRetrieve()
    {
        try
        {
            //connect
            Connection con = DriverManager.getConnection
               ("jdbc:mysql://localhost:8084/booking", "root", "");
            
            //make a query
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("Select customer.id, customer.name, customer.surname, categories.email from products inner join categories on products.category=categories.catId WHERE products.name LIKE '%"+jTextField1.getText()+"%'");
            //ResultSet rs = stmt.executeQuery("Select * from products");
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); //empty the table
            //output result
            while (rs.next())
            {
                model.addRow(new Object[]{rs.getInt(1),
                                   rs.getString(2),
                                   rs.getFloat(3),
                                   //rs.getInt(4));
                                   rs.getString(4)});
            }
            
            //close connection
            con.close();            
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }
